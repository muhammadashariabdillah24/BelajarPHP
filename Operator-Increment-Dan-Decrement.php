<!-- Operator Increment Digunakan Untuk menambahkan 1 angka pada variable -->

<!-- Operator Decrement Digunakan Untuk mengurangi 1 angka pada variable -->

<?php

    $angka = 10;
    echo "Sebelum Increment : $angka <br>";
    $angka++;
    echo "Setelah Increment : $angka <br>";
    $angka--;
    echo "Setelah Decrement : $angka";

?>