<!-- +=,  -=,  /=,  *= -->

<?php

    $uangBapak = 2000;
    $hargaAyam = 1000;

    // Ini ShortHand -=
    $uangBapak -= $hargaAyam;

    echo "Uang bapak setelah beli ayam = $uangBapak";

    // Ini ShortHand +=
    $uangBapak += 5000;

    echo "<br>";

    echo "Uang bapak setelah dapat bonus = $uangBapak";

?>