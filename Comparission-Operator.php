<?php

    // Comprassion ( == )
    $andi_bangun_jam = 4;

    if ($andi_bangun_jam == 4) {
        
        echo "Andi Bangun Jam : ".$andi_bangun_jam;

    } else {

        echo "Andi Tidak Bangun Jam : ".$andi_bangun_jam;
    }

    echo "<br>";

    // Comprassion ( != )
    $agung_minum = 'Kopi';

    if ($agung_minum != 'Kopi') {
        
        echo "Agung Tidak Minum : ".$agung_minum;

    } else {

        echo "Agung Miinum : ".$agung_minum;
    }

    echo "<br>";

    // Comprassion ( > )
    $nilai_dedi = 8;

    if ($nilai_dedi > 7) {
        
        echo "Nilai Dedi Lebih Dari : ".$nilai_dedi;

    } else {

        echo "Nilai Tidak Lebih Dari : ".$nilai_dedi;

    }

    echo "<br>";

    // Comprassion ( < )
    $nilai_asri = 7;

    if ($nilai_asri < 10) {
        
        echo "Nilai Asri : ".$nilai_asri;

    } else {

        echo "Nilai Asri Tidak Kurang Dari : ".$nilai_asri;
    }

    echo "<br>";

    // Comprassion ( >= )
    $nilai_neni = 10;

    if ($nilai_neni >= 10) {
        
        echo "Nilai Neni Lebih Besar Dari ".$nilai_neni." Atau Sama Dengan ".$nilai_neni;

    } else {

        echo "Nilai Neni Lebih Kecil Dari ".$nilai_neni." Atau sama dengan ".$nilai_neni;

    }

    echo "<br>";

    // Comprassion ( <= )
    $nilai_rata_rata_odi = 7;

    if ($nilai_rata_rata_odi <= 10) {
        
        echo "Nilai Rata Rata Odi Lebih Kecil Dari 10";

    } else {

        echo "Nilai Rata Rata Odi Lebih Besar Dari 10";

    }


?>